
// Open and close the booking room modal
let bookingRoom_modal = document.getElementById("booking-room-modal")
function openBookingRoomModal(){
    bookingRoom_modal.classList.add("open-booking-room-modal")
} //fonction pour ouvrir le modal
function closeBookingRoomModal(){
    bookingRoom_modal.classList.remove("open-booking-room-modal")
} //fonction pour le fermer